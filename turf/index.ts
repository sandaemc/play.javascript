import booleanPointInPolygon from '@turf/boolean-point-in-polygon';
import { point, polygon } from '@turf/helpers';
import { getCoords, getGeom, getType } from '@turf/invariant';
import { geomReduce } from '@turf/meta';

export const isCoordinateInsideGeofence = (coord: [number, number], geojson: string): boolean => {
  if (!coord) {
    throw new Error('NullArgument: coord');
  }

  return getPolygons(geojson).some(coords => booleanPointInPolygon(point(coord), polygon([coords])));
};

const getPolygons = (geojson: string | object): [number, number][][] => {
  if (!geojson) {
    throw new Error('NullArgument: geojson');
  }

  const geom = getGeom(typeof geojson === 'string' ? JSON.parse(geojson) : geojson);

  const initial: [number, number][][] = [];

  return geomReduce(geom, (_, current) => {
    switch (getType(current)) {
      case 'Polygon':
        initial.push(getCoords(current)[0]);
        break;
      case 'MultiPolygon':
        getCoords(current).forEach(coords => coords.forEach((coord: [number, number][]) => initial.push(coord)));
        break;
    }

    return initial;
  }, initial);
};



